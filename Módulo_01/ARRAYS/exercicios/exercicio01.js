const paises = ['Dinamarca', 'Noruega', 'Suiça', 'Espanha', 'Inglaterra']

// Adicionando país ao final do array

paises.push('Alemanha')
console.log(paises)

// Removendo o país ao final do array

paises.pop()
console.log(paises)

// Adicionando ao incio do array

paises.unshift('Ucrânia')
console.log(paises)

// Removendo ao incio do array

paises.shift()
console.log(paises)

// Imprimindo o último país do array

console.log(paises.length - 1)

// Imprimindo o segundo país do array

console.log(paises[1])

// Imprimindo o segundo país de indice 2 do array

console.log(paises[2])